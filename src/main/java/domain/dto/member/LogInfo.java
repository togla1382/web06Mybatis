package domain.dto.member;

public class LogInfo {
	private String email;
	private String name;
	
	public String getEmail() {
		return email;
	}
	public String getName() {
		return name;
	}
	
	public LogInfo(String email, String name) {
		this.email = email;
		this.name = name;
	}
	
	
	
	
}
